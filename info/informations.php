    <p>
        Group learning has been extensively studied. 
        Many of the benefits have been documented in scientific research. 
        So have other styles of teaching.
        So, let’s examine 5 of the known benefits of group learning.
    </p>
    <h2>
        Reduces anxiety
    </h2>
    <p>
        When a student is working on their own they have only their mind to rely on.
        This isn’t a bad thing in and of itself but, if a student is fixated on a problem they don’t understand they may be too embarrassed to ask for help from a teacher.
        One of the benefits of small group learning is the ability to speak with your peers for help. 
        Another way group learning activities helps to reduce anxiety is that the focus isn’t solely on them or on the material.
        They are engaged in what they are doing and part of a team. The attention is spread but no so much as to a student being ignored.
        The student ultimately worries less because they can focus on what they need to learn without feeling the pressure of the spotlight.
    </p>
    <h2>
        Develop Intrinsic Motivation
    </h2>
    <p>
        Intrinsic motivation is, essentially, the inner drive to do. 
        How group learning does that is by mutually inspiring students to achieve. 
        It’s one thing to study and learn because a teacher told you to. 
        It’s quite another to work on something with your peers to meet a goal. 
        It’s even something more when students can get lost in group learning activities. 
        They forget they are learning and become motivated by each other to complete the task.
        Developing intrinsic motivation carries onto everything else in life. 
        It’s a great way to keep students motivated and tends to come about in group learning. 
    </p>
    
